package class3;

public class RelationalOperators_6 {
    public static void main(String[] args) {
        /*
        A relational operator is a programming language construct or operator that tests or defines some kind of relation between two entities.
        Arithmetic operators:
        int+ int =int
        int/int=int
        int-int=int
        We always will get the answer True of False.
        > greater
        < less
        => less or equal
        <= greater or equal to
        == equal
        != not equal to



         */
        int number1=10;
        int number2 =10;
        System.out.println(number1+number2);
        boolean result =20>10;
        System.out.println(result);
        System.out.println(20>10); // we used here relational operator ">"
        System.out.println(5>10); // we used relational operator "<"
        System.out.println(5!=10); // we used relational operator "!=" (is 5 not equal to 10?)
        System.out.println(5==10); // is 5 equal to 10? We used relational operator '=="
        System.out.println(5==5);
        System.out.println(10>=20); //is 10 greater or equal to 20 ? -> False .Used operator ">="
        System.out.println(10<=20); // is 10 less or equal to 20 ? True
        System.out.println(10<=10); // is 10 less or equal to 10 ? True








    }
}
