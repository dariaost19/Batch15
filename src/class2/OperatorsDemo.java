package class2;

public class OperatorsDemo {
    public static void main(String[] args) {
        int number1=10;
        int number2=20;
        System.out.println(number1+number2);
        // number1+number2 = 30
        System.out.println(number1-number2);
        //number1 -number2 = -10
        System.out.println(number2-number1);
        //number2-number1= 10
        // To duplicate put cursor and press CTRL+D.
        System.out.println(number1*number2);
        //number1*number2 = 200
        System.out.println(number1/number2);
        System.out.println(number2/number1);
        // if you want to divide use (/)
        /*
        if you have to store whole numbers always use Int
        if you have to store decimal numbers always use Double
        if you have to string character you can always use String

         */
        



    }
}
