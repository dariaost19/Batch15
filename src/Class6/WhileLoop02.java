package Class6;

public class WhileLoop02 {
    public static void main(String[] args) {
        int number=1;
        while (number<5){
            System.out.println(number);
            number++;

        }
        int number2=0;
        while (number2<10){
            System.out.println(number2);
            number2+=2; // adding 2 to every number
        }

    }
}
