package PracticeJava;

public class DecreasingTriangle {
    public static void main(String[] args) {
        for (int i = 1; i <=6 ; i++) {
            for (int j =i; j <=6 ; j++) {
                System.out.print(j);

            }
            System.out.println();

        }
        System.out.println("********************************");
        for (int h = 1; h <=6 ; h++) {
            for (int f = h; f <=6 ; f++) {
                System.out.print("$");

            }
            System.out.println();

        }
    }
}
