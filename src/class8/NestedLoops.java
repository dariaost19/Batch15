package class8;

public class NestedLoops {
    public static void main(String[] args) {
        for (int i = 0; i <5 ; i++) { // outer For Loop
            for (int j = 0; j <5 ; j++) { // inner For Loop
                System.out.print(j + " ");

            }
            System.out.println();
        }
    }
}

